 <p class="mb-6">
Immediate, yet atmospheric. Wintry, yet warm. Evocative. Cinematic Midwesticana. Meticulous. Layered. Haunting. Widescreen panoramas of ever-shifting colors and textures in the background. *Redshift* feels like a night drive on the back roads of a moonsilvered soundscape, out where the county lines of art-folk, indie rock, and Americana meet.

 <p class="mb-6">The 12 tracks on this debut album materialized slowly, accreting over a years-long period during which songwriter/producer Jordan Keller incubated to refine his craft. “At the time it felt like writer’s block. But I was constantly writing, recording, and experimented.” He pulled lyrical influences from the short stories and poems of writers like Rick Bass, Alice Munro, and Steve Scafidi. He revisited albums from Doves, Sufjan Stevens, Hey Rosetta, and other bands he fell in love with out on long drives around his home state of Michigan.

 <p class="mb-6">It’s all-too-fitting, then, the  first sounds and image to emerge from the moonless intro of *Redshift*’s opening track “A Cross on a Gravel Road”: A hypnotically off-kilter drum beat mimicking the rhythm of a drive on a country highway. Warm beams of tremolo’d electric guitar over the horizon. “*The lone lights on the road*”. “Cross” lit the way for the rest of the album to follow. “That was the first song where every instrument and word felt vital to the story.” says Keller.

 <p class="mb-6">As profiled in an Atwood Magazine, each song “very much a story told as much through its music as it is through its lyrics.” Each song is a short film, a vignette of impressionistic details in 35mm close-ups. Instruments in the arrangements illuminate these details. Pewtery guitars and rumbling toms “billow and drum” in the stormy “Hallelujah Clouds”. Moonbeams of rhodes piano glisten on the black lake water of “Old Bruise”. The hammers of a felted upright piano clack like barren winter tree-branches in “Cracks.”

 <p class="mb-6">But as *Redshift*’s world was coming into focus, Keller’s was spinning on two axes. He was working through the personal disorientation of moving from his late 20s into his 30s,  while all of the political upheaval was roiling in America during the late 2010s and early ‘20s. “I was *really* feeling for the first time that things don’t always work out in our lifetimes. That the possibilities of our lives are constrained by our place in history.”

 <p class="mb-6">This sense of possibility and constraint is shared by all of the characters of *Redshift*. The resolve to not to collapse under the weight of their human experience. The despair of being pulled apart by blue-blind forces of nature and history. The restless yearning for truth and hope in the valleys between what their lives are and what they once hoped (or feared) those lives might be. The sense that it is not only into the past that the possibilities of life recede, it’salso the future.

 <p class="mb-6">Nowhere is this feeling of disorientation more evocatively captured than in the final verse the album’s centerpiece:

*“Now, I'm drunk under the glimmer of a thousand more stars.*

*And they'll go red as they grow dimmer till they're lost to the dark,*

 *I'll be restless in my searching,* 

*As heaven disappears,*

 *Man, I used to be so certain back when I belonged here.*

*So here’s to every nowhere village,*

*To the dark-hidden stars,*

*To the places god no longer visits,*

*Or sends his regards*

*I left here for a reason,*

*Way back then it wasn’t clear,*

*With some distance now I see it,*

*I don’t wanna belong here”*

These final lines are the final steps off the bridge that spans the album’s rearview-mirror-looking perspective, and leads into the album’s future-eyeing back half. It starts with Hallelujah Cloud’s rise out of despair of a prayer to “nothing there” and shifts into a rumination “There must be something here” in the stormy and gospel-tinged “Hallelujah Clouds” It morphs into open-ended questions of “Old Bruise” whose narrator dangles their feet over the precipice of despair before:

*“I’m poring over everything,*

*I’ve got regrets ahead of me,*

*And I’m wondering which ones I’ll choose*

*And which choose me instead,*

*What will I have when I’m looking back?*

*Once what’s ahead is the distant past?*

*The sum of a life that I left to chance?*

*Man, I dunno, but I don’t wanna waste away.”*

“For a while I thought of the album as a survivor’s guide for people I’d known or been in my 20s. People who were content to waste away. To rot. To give up. To not. I thought I could hand these song backwards through time to  guide some past version of myself. But the more Keller sat with these characters and figured out what they wanted to say, the more he felt he wasn’t providing preachy guidance to his past selves. They were articulating questions to guide him in the present.

The stirring “Till the End Time Begins”—the last song Keller finished for the album—marks the end of the period. I wrote that song for someone who felt the world was going to shit and was basically drinking themselves to death. It took a long time for me to figure out what I would ask them to get them “The question “What do we do till then, till the end time begins”

“You have to use the substance your life. You can’t let the despair drag you down.”

“Each of the characters in these songs feel tied to a pretty specific chapter of my 20s. The album is inseparable from the seasons of life I had to live through in order to create it.”

A soundtrack for taking the long way home: *Redshift* contains worlds to get lost in—again and again and again.”If there’s a unified answer for all of Redshift’s big questions, it’s phrased in a question: How do we maintain hope in the midst of hopelessness. Hope for ourselves, yes. But—more importantly—for others. 

*“You kick like hell and save yourself somehow,*

*Knowing you’ll drown,*

*Clinging to hope,*

*That you might make it out,*

*And if you kick till you hit,*

*Grab a handful of mud,*

*To prove you made it down.”*</p>

